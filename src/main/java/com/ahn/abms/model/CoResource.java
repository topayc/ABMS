package com.ahn.abms.model;

public class CoResource {
	private int coResourceNo;
	private String orgFileName;
	private String newFileName;
	private String fileName;
	private String fileSize;
	private String localAccessPath;
	private String webAceessPath;
	private String createTime;
	public int getCoResourceNo() {
		return coResourceNo;
	}
	public void setCoResourceNo(int coResourceNo) {
		this.coResourceNo = coResourceNo;
	}
	public String getOrgFileName() {
		return orgFileName;
	}
	public void setOrgFileName(String orgFileName) {
		this.orgFileName = orgFileName;
	}
	public String getNewFileName() {
		return newFileName;
	}
	public void setNewFileName(String newFileName) {
		this.newFileName = newFileName;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileSize() {
		return fileSize;
	}
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	public String getLocalAccessPath() {
		return localAccessPath;
	}
	public void setLocalAccessPath(String localAccessPath) {
		this.localAccessPath = localAccessPath;
	}
	public String getWebAceessPath() {
		return webAceessPath;
	}
	public void setWebAceessPath(String webAceessPath) {
		this.webAceessPath = webAceessPath;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	
}
